#include <stdio.h>

int add(int a, int b) {
    return a + b;
}

int subtract(int a, b) {
    return a - b;
}

int multiply(int a, b) {
    return a * b;
}

int main() {
    printf("Addition result: %d\n", add(5, 3));
    printf("Subtraction result: %d\n", subtract(5, 3));
    printf("Multiplication result: %d\n", multiply(5, 3));
    return 0;
}
